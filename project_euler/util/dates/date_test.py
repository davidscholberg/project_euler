from unittest import TestCase

from project_euler.util.dates.date import Date

class TestDate(TestCase):
    def test_date(self):
        self.assertEqual(Date(1900, 1, 1), Date(1900, 1, 1))
        self.assertNotEqual(Date(1800, 1, 1), Date(1900, 1, 1))
        self.assertGreater(Date(1900, 1, 2), Date(1900, 1, 1))
        self.assertGreater(Date(1900, 2, 1), Date(1900, 1, 2))
        self.assertGreater(Date(1901, 1, 1), Date(1900, 2, 1))
        self.assertLess(Date(1900, 2, 28), Date(2001, 3, 2))
        self.assertGreaterEqual(Date(1901, 1, 1), Date(1901, 1, 1))
        self.assertGreaterEqual(Date(1901, 1, 2), Date(1901, 1, 1))
        self.assertLessEqual(Date(1901, 1, 1), Date(1901, 1, 1))
        self.assertLessEqual(Date(1901, 1, 1), Date(1901, 1, 2))
        date = Date(2022, 11, 1)
        self.assertEqual(30, date.days_to_first_of_next_month())
        self.assertEqual(61, date.days_to_first_of_next_year())
        date = Date(2022, 11, 7)
        self.assertEqual(13, date.days_to(Date(2022, 11, 20)))
        self.assertEqual(43, date.days_to(Date(2022, 12, 20)))
        self.assertEqual(67, date.days_to(Date(2023, 1, 13)))
        self.assertEqual(4557, date.days_to(Date(2035, 4, 30)))
        self.assertEqual(1, Date(1900, 1, 1).day_of_week)
        self.assertEqual(0, Date(1899, 12, 31).day_of_week)
        self.assertEqual(2, Date(1900, 1, 2).day_of_week)
        self.assertEqual(6, Date(1935, 7, 20).day_of_week)
        self.assertEqual(0, Date(1751, 9, 5).day_of_week)
        self.assertEqual(2, Date(2022, 11, 1).day_of_week)
        date = Date(2022, 11, 1)
        date.add_days(1)
        self.assertEqual(Date(2022, 11, 2), date)
        self.assertEqual(3, date.day_of_week)
        date.add_days(6)
        self.assertEqual(Date(2022, 11, 8), date)
        self.assertEqual(2, date.day_of_week)
        date.add_days(7)
        self.assertEqual(Date(2022, 11, 15), date)
        self.assertEqual(2, date.day_of_week)
        date.add_days(8)
        self.assertEqual(Date(2022, 11, 23), date)
        self.assertEqual(3, date.day_of_week)
        date.add_days(20)
        self.assertEqual(Date(2022, 12, 13), date)
        self.assertEqual(2, date.day_of_week)
        date.add_days(90)
        self.assertEqual(Date(2023, 3, 13), date)
        self.assertEqual(1, date.day_of_week)